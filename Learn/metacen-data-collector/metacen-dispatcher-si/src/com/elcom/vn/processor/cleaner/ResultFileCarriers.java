package com.elcom.vn.processor.cleaner;

/**
 *
 * @author anhdv
 */
public class ResultFileCarriers {

    private Double frequency;
    
    private Double symbolrate;
    
    public ResultFileCarriers() {
    }

    /**
     * @return the frequency
     */
    public Double getFrequency() {
        return frequency;
    }

    /**
     * @param frequency the frequency to set
     */
    public void setFrequency(Double frequency) {
        this.frequency = frequency;
    }

    /**
     * @return the symbolrate
     */
    public Double getSymbolrate() {
        return symbolrate;
    }

    /**
     * @param symbolrate the symbolrate to set
     */
    public void setSymbolrate(Double symbolrate) {
        this.symbolrate = symbolrate;
    }
}
